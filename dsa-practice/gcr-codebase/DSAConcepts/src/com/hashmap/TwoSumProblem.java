package com.hashmap;

import java.util.HashMap;
import java.util.Scanner;

public class TwoSumProblem {

    // Method to find two indices such that their values add up to the target
    public static int[] twoSum(int[] nums, int target) {

        HashMap<Integer, Integer> map = new HashMap<>();

        for (int i = 0; i < nums.length; i++) {

            int complement = target - nums[i];

            if (map.containsKey(complement)) {
                return new int[]{map.get(complement), i};
            }

            map.put(nums[i], i);
        }

        return new int[0];
    }

    // Main method
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        // Input array size
        System.out.print("Enter number of elements: ");
        int n = sc.nextInt();

        int[] nums = new int[n];

        // Input array elements
        System.out.println("Enter array elements:");
        for (int i = 0; i < n; i++) {
            nums[i] = sc.nextInt();
        }

        // Input target sum
        System.out.print("Enter target sum to check: ");
        int target = sc.nextInt();

        // Find result
        int[] result = twoSum(nums, target);

        // Output
        System.out.println("\n--- Two Sum Result ---");
        if (result.length == 2) {
            System.out.println("Target Sum : " + target);
            System.out.println("Indices    : " + result[0] + ", " + result[1]);
            System.out.println("Values     : " + nums[result[0]] + " + " + nums[result[1]]
                    + " = " + target);
        } else {
            System.out.println("No two numbers add up to " + target);
        }

        sc.close();
    }
}
