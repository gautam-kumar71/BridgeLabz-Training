//Create AgeOfHarry Class to calculate age of Harry
public class AgeOfHarry{
    public static void main(String args[])
    {
        //Create a variable currentYear and birthYear to store the current year
        int currentYear=2024;
        int birthYear=2000; 
        
        // Calculate Harry's age
        int harryAge=currentYear-birthYear;
        
        //Display the calculated age of Harry
        System.out.println("Harry's age in 2024 is "+harryAge);
    }
}
